"""PHP extension catalog and per-site Dockerfile rendering.

Extensions are installed on top of the site's base PHP image using the
``install-php-extensions`` helper (https://github.com/mlocati/docker-php-extension-installer),
which handles system dependencies automatically. The site's PHP service switches
to a small ``build`` context only when at least one extension is selected, so
sites without custom extensions keep using the plain image unchanged.
"""

